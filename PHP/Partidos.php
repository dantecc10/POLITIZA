        <h3 class="TítuloArtículo">¿Qué es un partido político?</h3>
        <h4 class="Autor-Artículo">Por Dante Castelán Carpinteyro.</h4>
        <p class="Descripción-Artículo">Un partido político es un grupo de ciudadanos que está constituido legalmente,
            según los requisitos y el orden jurídico vigente. En México, los partidos políticos deben ser registrados
            por la autoridad electoral como tal, y es necesario para su supervivencia que cuenten miembros afiliados, al
            menos el 2.4% del padrón electoral nacional; eso significa que de todos los mexicanos que tienen la mayoría
            de edad (alrededor de 90 millones).</p>
        <p class="Descripción-Artículo">Los partidos políticos tienen dos objetivos principales:</p>
        <ol class="Descripción-Artículo">Objetivos: <li class="ElementoDeLista">Canalizar y transmitir los intereses y
                demandas de la población para que sean consideradas en la toma de decisión gubernamental.</li>
            <li class="ElementoDeLista">Posibilitar la participación de la población en el proceso político por medio de
                la elección de los representantes populares que ejercen el poder político.</li>
        </ol>
        <p class="Autor-Artículo">Fuente: <i>Partido político</i>, en <i>Gobernación</i>. Disponible en:
            <a>http://sil.gobernacion.gob.mx/Glosario/definicionpop.php?ID=178</a>
        </p>
        <h3 class="TítuloArtículo">Partidos políticos nacionales</h3>
        <p class="Descripción-Artículo">México es una democracia joven, y con un historial partidista bastante complejo
            en la dinámica política y social. A continuación se presentan los partidos políticos con presencia nacional,
            ordenados por fuerza de forma descendente:</p>
        <h4 class="TítuloArtículo">Morena</h4>
        <p class="Descripción-Artículo"> <img class="LogoPartidista" id="LogoMorena"
                src="../IMG/Partidos/Morena-LOGO.png"  align="left"> <b>Movimiento de Regeneración
                Nacional</b><br>Morena fue creado como una asociación civil en el 2011; tiene por
            nombre
            <i>Movimiento de Regeneración Nacional</i> y obtuvo su registro como partído político en julio de 2014. El
            partido se declara de <b>izquierda</b>, y tiene como principios de defensa la democracia, el combate a la
            corrupción, y al <b>neoliberalismo</b>. <br> Actualmente, Morena es la principal fuerza política de México,
            sobretodo por la corriente dominante en el partido: el <i>lopezobradorismo</i>, encabezada por el Presidente
            de México, Andrés Manuel López Obrador.
        </p>
        <h4 class="TítuloArtículo">PAN</h4>
        <p class="Descripción-Artículo"> <img class="LogoPartidista" id="LogoPAN" src="../IMG/Partidos/PAN_Logo.png"
                 align="left"> <b>Partido Acción Nacional</b>. <br>El PAN es la principal fuerza opositora
            a nivel
            nacional; son una facción de derecha, pero en uno de sus últimos congresos nacioanles, se cambiaron -según
            su <i>declaración de principios</i> a la socialdemocracia. El PAN fue el primer partido en llegar a la
            Presidencia de la República en el 2000, "acabando" con la hegemonía priísta de más de 70 años. El Partido
            Acción Nacional es la segunda fuerza política, sólo superada por Morena, esto tomando en cuenta su
            representación en los órganos legislativos. </p>
        <h4 class="TítuloArtículo">PRI</h4>
        <p class="Descripción-Artículo"> <img class="LogoPartidista" src="../IMG/Partidos/PRI_Logo.png" id="LogoPRI"
                align="left"> El <b>Partido Revolucionario Institucional</b>. <br> El PRI es uno de los
            primeros
            partidos políticos mexicanos, fruto -por supuesto- de la evolución de otros partidos en la época
            post-revolucionaria. <br> Mantuvo un gobierno hegemónico por más de 70 años hasta que el PAN llegó a la
            Presidencia en el 2000. <br> El PRI es la tercera fuerza política de México en el poder legislativo, aunque
            lo cierto es que lleva una larga racha de derrotas en las elecciones por gubernaturas estatales: por
            ejemplo, en 2021, el PRI perdió contra la coalición "Juntos Hacemos Historia" 8 de las 8 gubernaturas que
            gobernaba y que estaban en juego. <br> Este partido ha tenido épocas muy distintas entre sí; ha tenido
            varias de las figuras políticas más relevantes del siglo pasado, como el Gral. Lázaro Cárdenas, Adolfo López
            Mateos, Plutarco Elías Calles, Carlos Salinas de Gortari, Luis Donaldo Colosio, e incluso actuales figuras
            de la <i>Cuarta Transformación</i>, entre ellos Manuel Bartlett Díaz, etc. </p>
        <h4 class="TítuloArtículo">PVEM</h4>
        <p class="Descripción-Artículo"> <img class="LogoPartidista" id="LogoPVEM" src="../IMG/Partidos/PVEM_Logo.png"
                align="left"> El <b>Partido Verde Ecologista de México</b>. <br> El PRI es uno de los
            primeros partidos políticos mexicanos, fruto -por supuesto- de la evolución de otros partidos en la época
            post-revolucionaria. <br> Mantuvo un gobierno hegemónico por más de 70 años hasta que el PAN llegó a la
            Presidencia en el 2000. <br> El PRI es la tercera fuerza política de México en el poder legislativo, aunque
            lo cierto es que lleva una larga racha de derrotas en las elecciones por gubernaturas estatales: por
